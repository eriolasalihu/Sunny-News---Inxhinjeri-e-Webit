                <p>Albina Kelmendi dhe familja e saj do ta përfaqësojnë Shqipërinë në Eurovision.

                    Kjo pasi me këngën “Duje”, Albina me familje e rrëmbeu fitoren e këtij festivali, transmeton Klan Kosova.
                    
                    Festivali i këngës në RTSH përgjatë këtyre tre netëve erdhi me përolotë surpriza dhe këngë e interpretime mjaft të bukura.
                    
                    Falë votave të publikut, këngëtarja nga Kosova, Albina dhe Familja Kelmendi kanë arritur të fitojnë Festivalin e Këngës së Radio Televizionit Shqiptar, duke u bërë kështu zyrtarisht artistët prezantues të Shqipërisë në edicionin e radhës të “Eurosong Contest 2022”.
                    
                    Për dallim nga publiku, juria e edicionit të 61-të të këtij kompeticioni i ka dhuruar çmimin e parë këngëtares Elsa Lila, e cila këtë vit konkurroi me këngën “Evita”.
                    
                    Ndërkaq, vendin e tretë e ka rrëmbyer 2Farm me këngën “Atomike”.
                    
                    Gara e këtij viti ishte goxha e ngushtë në mes të Lilës dhe Kelmendit, por edhe një emër tjetër që është përfolur shumë për fitore ishte Fifi, që sivjet u paraqit me këngën “Stop”.
                    
                    Për herë të parë, kënga që do të përfaqësojë Shqipërinë në Eurovision 2023 u zgjodh vetëm me votat e publikut me anë të SMS-ve. Rezultoi që “Duje” ishte e preferuara e publikut.</p>
